'''
Collatz猜想也叫3n+1猜想，给一个正整数，如果是偶数，则减半；
如果是奇数，则变为它的三倍加一。直到变为1停止。猜想对于所有正整数经过足够多次变换最终达到1。‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬

'''
def Cal(n):
    if n % 2 == 0:
        return n / 2
    else:
        return 3 * n + 1
n = int(input())
while n != 1:
    n = Cal(n)
    print("{:.1f}".format(n))