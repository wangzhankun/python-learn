'''
一只青蛙一次可以跳上1级台阶，也可以跳上2级。
请问该青蛙跳上一个n级的台阶总共有多少种跳法。‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬
输入台阶数，输出一共有多少种跳法‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬

注意：如果运算超时，请思考有什么办法降低时间复杂度。
'''


def cal(num):
    lst = [1, 2, 3]
    i = 2
    while num > i:
        lst.append(lst[-1] + lst[-2])
        i += 1
        del(lst[0])
    return lst.pop(-2)


num = int(input())
print(cal(num))
