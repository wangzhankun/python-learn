'''
接收一个列表，找出其中数量最多的元素，
如果元素数量相同，选择列表中靠前的元素输出。‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬
示例1: [3,2,3]‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‫‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‮‬
'''
counts = {}  # 空字典
num = eval(input())  # 格式化拿到输入，把语句转化为python语句，此时num就是list
for i in num:
    counts[i] = counts.get(i, 0)+1
items = list(counts.items())  # 返回一个包含所有字典项的列表
items.sort(key=lambda x: x[1], reverse=True)
NUM, ss = items[0]
print(int(NUM))
